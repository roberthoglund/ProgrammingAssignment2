## makeCacheMatrix and cacheSolve are two functions that together creates
## a system that can cache the inverse of a matrix. Note that there is no
## error handling so the matrix passed into makeCacheMatrix must solveable.

## Creates a list that stores the matrix as well as the cached inverse

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}

## Cache the inverse of the matrix stored in x

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
}
